/*
second system extends operation.
*/
public class SecondSystem extends Operation {
    @Override
    int SumOfTwoNumbers(int a,int b){
        return a+b;
    }
    @Override
    int SubtractionOfTwoNumbers(int a,int b)
    {
        return a-b;
    }
    @Override
    int MultiplicationOfTwoNumbers(int a,int b){
        return a*b;
    }
}
