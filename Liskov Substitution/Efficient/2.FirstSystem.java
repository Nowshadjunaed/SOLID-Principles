
public class FirstSystem extends Operation {
    @Override
    int SumOfTwoNumbers(int a,int b){
        return a+b;
    }
    @Override
    int SubtractionOfTwoNumbers(int a,int b)
    {
        return a-b;
    }
    @Override
    int MultiplicationOfTwoNumbers(int a,int b){
        return a*b;
    }
    @Override
    int RemainderOfTwoNumbers(int a,int b){
        return a%b;
    }
}
